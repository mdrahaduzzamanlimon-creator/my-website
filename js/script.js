const perPage = 20;
let currentPage = 1;

function renderPacks() {
  const container = document.getElementById("packContainer");
  container.innerHTML = "";

  let filtered = packs.filter(pack =>
    pack.title.toLowerCase().includes(document.getElementById("searchInput").value.toLowerCase())
  );

  const start = (currentPage - 1) * perPage;
  const end = start + perPage;
  const paginated = filtered.slice(start, end);

  paginated.forEach(pack => {
    const card = document.createElement("div");
    card.className = "card";
    card.innerHTML = `
      <img src="${pack.image}">
      <div class="card-content">
        <h3>${pack.title}</h3>
        <p>${pack.description}</p>
      </div>
      <a href="${pack.link}" class="download-btn" target="_blank">DOWNLOAD</a>
    `;
    container.appendChild(card);
  });

  renderPagination(filtered.length);
}

function renderPagination(totalItems) {
  const pagination = document.getElementById("pagination");
  pagination.innerHTML = "";

  const totalPages = Math.ceil(totalItems / perPage);
  for (let i = 1; i <= totalPages; i++) {
    const btn = document.createElement("button");
    btn.innerText = i;
    if (i === currentPage) btn.style.background = "#0056b3";
    btn.onclick = () => { currentPage = i; renderPacks(); };
    pagination.appendChild(btn);
  }
}

function searchPacks() {
  currentPage = 1;
  renderPacks();
}

